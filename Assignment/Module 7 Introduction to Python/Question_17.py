# Q17. What are negative indexes and why are they used?
# Answer:

# Negative indexes in Python are used to access elements from the end of a list, string, or tuple.
# -1 refers to the last element, -2 to the second last, and so on.

text="Aditya"
print(text[-1])
print(text[-2])
print(text[-3])
print(text[-4])
print(text[-5])
print(text[-6])
